package user;


public class user {


    private int id;
    private String name;
    private String email;
    private String phone;
    private String username;
    private String password;
    
  

	public int getid() {
		return id;
	}





	public String getname() {
		return name;
	}





	public String getemail() {
		return email;
	}



	



	public String getphone() {
		return phone;
	}



	



	public String getusername() {
		return username;
	}



	



	public String getpassword() {
		return password;
	}



	



	public user(int id, java.lang.String name, java.lang.String email, java.lang.String phone,
			java.lang.String username1, java.lang.String password) {
		super();
		this.id = id;
		this.name = name;
		this.email = email;
		this.phone = phone;
		this.username = username1;
		this.password = password;
	}

  
}

